public class CharExamples {
    public static void main(String[] args) {
        //single quotes
        //Java primitive char supports 16 bit Unicode (Basic)
        char myChar = 'x';
        System.out.println(myChar);

        //JAVA UNICODE
        char myUnicode = '\u018A';
        System.out.println(myUnicode);

        //Boolean
        boolean isWeekday = true;
        boolean isWarm = false;
        System.out.println("Is Weekday? " + isWeekday + ", Is warm? " + isWarm);
    }
}
